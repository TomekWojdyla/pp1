#13.	Using functions and constants available in the 'math' module, write a program that calculates and displays:
#a.	natural logarithm of 5
#b.	e raised to the power of 3
#c.	square root of 7
#d.	sine of 90 degrees

import math

#task a
print("natural logarithm of 5 is: ", math.log(5))

#task b
print("e raised to the power of 3 is: ", math.exp(3))

#task c
print("square root of 7 is: ", math.sqrt(7))

#task d
print("sine of 90 degrees is: ", math.sin(90*2*math.pi/360))